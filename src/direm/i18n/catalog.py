def supported_language_codes() -> tuple[str, ...]:
    return tuple(language.value for language in LanguageCode)


def normalize_language_code(language_code: str | None) -> str:
    if not language_code:
        return DEFAULT_LANGUAGE

    normalized = language_code.lower().split("-", maxsplit=1)[0]
    if normalized in supported_language_codes():
        return normalized

    return DEFAULT_LANGUAGE


def language_name(language_code: str | None) -> str:
    return LANGUAGE_NAMES[normalize_language_code(language_code)]


def t(language_code: str | None, key: str, **kwargs: object) -> str:
    language = normalize_language_code(language_code)
    try:
        template = CATALOG[language][key]
    except KeyError:
        template = CATALOG[DEFAULT_LANGUAGE][key]
    return template.format(**kwargs)
